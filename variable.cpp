// write a c++ program to declare different kind c++ variable
//syntax : type variable=value;

#include <iostream>
using namespace std;

int main()
{
    // printing integer value
    int my_num=15;
    my_num=10;

    // printing double or float value
    double myfloatnum=3.14;

    //printinng character
    char mytext='J';

    //printing string
    string mystr="Hello";

    //printing boolean
    bool myBoolean=true;

    cout << my_num << endl;
    cout << myfloatnum << endl;
    cout << mytext << endl;
    cout << mystr << endl;
    cout << myBoolean <<endl;
}